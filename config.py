@dataclass(frozen=True, slots=True)
class ParamDef:
    key: str
    label: str
    is_float: bool
    min_val: float
    max_val: float
    default: float
    step: float


@dataclass(frozen=True, slots=True)
class PatternDef:
    name: str
    fn: Callable
    description: str
    params: tuple[ParamDef, ...] = ()
    needs_backend_dispatch: bool = False


